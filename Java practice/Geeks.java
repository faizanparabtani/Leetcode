// == operator for compatible data types
class Geeks {
    public static void main(String[] args) {
        
        // integer-type
        System.out.println(10 == 10);

        // char-type
        System.out.println('a' == 'b');

        // char and double type
        System.out.println('a' == 97.0);

        // boolean type
        System.out.println(true == false);
    }
}